    def get_text(self, key: str) -> str:
        """
        Get translated text for the given key in the current language.
        Falls back to English if key not found in current language.
        """
        try:
            # Try to get text in current language
            if self.current_language in self.translations:
                current_lang_dict = self.translations[self.current_language]
                if key in current_lang_dict:
                    return current_lang_dict[key]

            # Fallback to English
            if "en" in self.translations and key in self.translations["en"]:
                return self.translations["en"][key]

            # Ultimate fallback - return the key itself
            return key.replace("_", " ").title()

        except Exception as e:
            safe_print(f"Translation error for key '{key}': {e}")
            return key.replace("_", " ").title()

    def get_current_language(self) -> str:
        """Return the current language code."""
        return self.current_language

    def set_language(self, language_code: str) -> bool:
        """
        Set the current language.
        Returns True if successful, False if language not supported.
        """
        if language_code in self.available_languages:
            self.current_language = language_code
            safe_print(f"Language set to: {self.available_languages[language_code]}")
            return True
        else:
            safe_print(f"Language '{language_code}' not supported.")
            return False

    def select_language(self) -> str:
        """
        Interactive language selection menu.
        Returns the selected language code.
        """
        safe_print("\n" + "=" * 60)
        safe_print("🌍 LANGUAGE SELECTION / SELECCIÓN DE IDIOMA")
        safe_print("=" * 60)

        # Display available languages
        languages_list = list(self.available_languages.items())
        for i, (code, name) in enumerate(languages_list, 1):
            safe_print(f"{i}. {name}")

        while True:
            try:
                choice = input(f"\nSelect language (1-{len(languages_list)}): ").strip()

                if choice.isdigit():
                    choice_num = int(choice)
                    if 1 <= choice_num <= len(languages_list):
                        selected_code = languages_list[choice_num - 1][0]
                        selected_name = languages_list[choice_num - 1][1]

                        self.set_language(selected_code)
                        safe_print(f"✅ {self.get_text('language_selected')}: {selected_name}")
                        safe_print(f"✅ Language selected: {selected_name}")
                        return selected_code
                    else:
                        safe_print("❌ Invalid choice. Please try again.")
                else:
                    safe_print("❌ Please enter a number.")

            except KeyboardInterrupt:
                safe_print("\n👋 Using default language: English")
                self.set_language("en")
                return "en"
            except Exception as e:
                safe_print(f"❌ Error: {e}. Using default language: English")
                self.set_language("en")
                return "en"

    def get_available_languages(self) -> Dict[str, str]:
        """Return dictionary of available language codes and names."""
        return self.available_languages.copy()

    def is_language_supported(self, language_code: str) -> bool:
        """Check if a language code is supported."""
        return language_code in self.available_languages

    def get_language_name(self, language_code: str) -> str:
        """Get the display name for a language code."""
        return self.available_languages.get(language_code, "Unknown Language")

    def save_language_preference(self, filename: str = "language_preference.json") -> bool:
        """Save current language preference to file."""
        try:
            preference = {
                "language": self.current_language,
                "language_name": self.get_language_name(self.current_language)
            }
            with open(filename, 'w', encoding='utf-8') as f:
                json.dump(preference, f, indent=2, ensure_ascii=False)
            return True
        except Exception as e:
            safe_print(f"Error saving language preference: {e}")
            return False

    def load_language_preference(self, filename: str = "language_preference.json") -> bool:
        """Load language preference from file."""
        try:
            with open(filename, 'r', encoding='utf-8') as f:
                preference = json.load(f)

            language_code = preference.get("language", "en")
            if self.is_language_supported(language_code):
                self.set_language(language_code)
                return True
            else:
                safe_print(f"Saved language '{language_code}' not supported. Using English.")
                self.set_language("en")
                return False

        except FileNotFoundError:
            # No preference file exists yet - this is normal
            return False
        except Exception as e:
            safe_print(f"Error loading language preference: {e}")
            return False

